    private void addArchivoActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_addArchivoActionPerformed
        double pesoArchivo = Double.parseDouble(this.sizeFile.getText());
        Archivo archivo = new Archivo();
        archivo.setNombre(this.nameFile.getText());
        archivo.setTama単o(pesoArchivo / 1024);
        if (this.RPD.isPower()) {
            if (this.ssdRB.isSelected()) {
                RA.agregarArchivoSSD(archivo, cargaArchivo);
            } else if (this.discoRB.isSelected()) {
                RA.agregarArchivoDiscoDuro(archivo, cargaArchivo);
            } else {
                JOptionPane.showMessageDialog(null, "Seleccione donde se guardara el archivo");
            }
        } else {
            JOptionPane.showMessageDialog(null, "No se puede realizar operacion,\nEncienda el computador");
        }
// A単adir un retraso de 1 segundo antes de actualizar la vista
        Timer timer = new Timer(1000, e -> mostrar());
        timer.setRepeats(false); // No repetir el timer
        timer.start();
    }//GEN-LAST:event_addArchivoActionPerformed

    public void extaerArchivo() {
        addArchivo.setEnabled(false);
        if (this.TArchivos.getSelectedRow() != -1) {
            this.nameFile.setText("");
            this.sizeFile.setText("");
        }
    }

    public final void mostrar() {
        DefaultTableModel modelo = (DefaultTableModel) this.TArchivos.getModel();
        modelo.setRowCount(0); // Limpiar todas las filas existentes en la tabla

        var archivosGuardados = this.RSD.getStoragedevice();

        for (StorageDevice device : archivosGuardados) {
            List<Archivo> archivos = device.getArchivos();
            if (archivos == null) {
                archivos = new ArrayList<>(); // Inicializa la lista si es null
            }

            for (Archivo archivo : archivos) {
                Object[] rowData = {
                    archivo.getNombre(),
                    archivo.getTama単o() * 1024 + "MG", // Convertir tama単o a MG
                    device.getTipo()
                };
                modelo.addRow(rowData); // Agregar una nueva fila al modelo
            }
        }
    }

