    public void isPCPower() {
        if (this.RPC.pcIsCreated() == true && !this.RPC.isPower()) {
            this.RBoard.setText("Computador creado, se encuentra en estado apagado");
            this.RBoard.setForeground(Color.RED);
        } else if (this.RPC.pcIsCreated() == true && this.RPC.isPower()) {
            this.RBoard.setText("Computador Encendido");
            this.RBoard.setForeground(Color.GREEN);
            this.powerPC.setEnabled(false);
            this.apagerPc.setEnabled(true);
        } else if (!this.RPC.pcIsCreated() == true) {
            this.powerPC.setEnabled(false);
            this.verDetalle.setEnabled(false);
            this.remplazarComponente.setEnabled(false);
            this.addFile.setEnabled(false);
            this.RBoard.setText("Agregue componentes para poder crear el PC");
            RBoard.setForeground(Color.BLACK);
            this.powerPC.setEnabled(false);
            this.apagerPc.setEnabled(false);
        }
    }

